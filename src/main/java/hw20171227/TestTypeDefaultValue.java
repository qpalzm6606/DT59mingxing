package hw20171227;

public class TestTypeDefaultValue {

	public static void main(String[] args) {
		TypeDefaultValue r = new TypeDefaultValue();
		System.out.println(r.a);
		System.out.println(r.b);//char类型的默认值是空格
		System.out.println(r.c);//double类型的默认值是0.0
		System.out.println(r.d);
		System.out.println(r.e);
		System.out.println(r.f);//布尔类型的默认值是false
		System.out.println(r.g);//float的默认值0.0
		System.out.println(r.h);//数值类的默认值都是0
		System.out.println(r.power);//String默认值是null	
		System.out.println(r.drg);// 集合的默认值一是串不明代码
	}

}
